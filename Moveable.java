// Define the Moveable interface
public interface Moveable
{
    // Method to set the position of the object
    public void setPos(int x, int y);

    // Method to set the x-coordinate of the object
    public void setX(int x);

    // Method to set the y-coordinate of the object
    public void setY(int y);

    // Method to get the x-coordinate of the object
    public int getX();

    // Method to get the y-coordinate of the object
    public int getY();

    // Method to get the width of the object
    public int getWidth();

    // Method to get the height of the object
    public int getHeight();

    // Method to set the width of the object
    public void setWidth(int w);

    // Method to set the height of the object
    public void setHeight(int h);
}
